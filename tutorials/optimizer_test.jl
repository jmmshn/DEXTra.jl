using JuMP, Ipopt

# ╔═╡ 0d846381-1885-4097-a981-387cb9293c4e
begin
	N_orders = 4
	N_currencies = 3
	model = Model(Ipopt.Optimizer)
	x = @variable(model, x[1:N_orders], lower_bound = 0)
	y = @variable(model, y[1:N_orders], lower_bound = 0)
	p = @variable(model, p[1:N_currencies], lower_bound = 0)
end

# ╔═╡ e05b09db-c6b4-4546-85ef-55e8a73d6211
x_max = repeat([10], length(x))

# ╔═╡ 1df8a992-146e-41f7-8cbc-82067b5fd1b4
y_max = repeat([10], length(y))

# ╔═╡ 64c209af-25cc-4dc2-9284-83c35aede3a1
p_max = repeat([10], length(p))

# ╔═╡ 1924af66-12cf-4852-a09b-a17bfc755436
@constraint(model, x .<= x_max)

# ╔═╡ d392242a-6e90-43e8-96a5-5b762d6a59ae
@constraint(model, y .<= x_max)

# ╔═╡ 3c2d4bfa-7866-4305-a933-fcdb27930c9e
@constraint(model, p .<= p_max)

# ╔═╡ 22b1cfe4-e90b-4690-83eb-838c7105d6a5
begin
	T_buy = [rand(1:length(p)) for i in 1:length(x)]
	T_sell = [((T_buy[i] - 1 + rand(1:length(p) - 1)) % length(p)) + 1 for i in 1:length(x)]
end

# ╔═╡ 2e40268a-632e-4bba-ad2c-0578a4c8a18d
buy_amts = [p[curr] * xi for (curr, xi) in zip(T_buy, x)]

# ╔═╡ 23b22937-904c-40de-a71b-4b36cdb288db
sell_amts = [p[curr] * yi for (curr, yi) in zip(T_sell, y)]

# ╔═╡ 5b8422f6-a467-49db-aecd-81d1d6e2641f
@constraint(model, p .<= p_max)

# ╔═╡ 8f62ae48-c13e-4d43-b8f5-787d65afb62e
@constraint(model, buy_amts.== sell_amts)

# ╔═╡ 5491aaab-408d-427f-bb12-ca9651f426a3
price_limits = [rand() for _ in 1:length(x)]

# ╔═╡ f4390e6a-46e9-4cdc-9549-52374b9fdb5e
@constraint(model, y .<= price_limits.*x)

# ╔═╡ c0db392f-c109-428c-b30e-4b4beaaf0e20
set_objective(model, MOI.MAX_SENSE, sum(buy_amts))

# ╔═╡ b60527ad-05fe-4772-967d-55a578f11330
